class User extends Authenticatable
{
    use HasApiTokens, HasFactory, Notifiable;

    /**
     * The table associated with the model.
     *
     * @var string
     */
    protected $table = 'd_users';

    /**
     * The attributes that are mass assignable.
     *
     * @var array<int, string>
     */
    protected $fillable = [
        'name',
        'email',
        'password',
        'user_role',
        'status'
    ];

    /**
     * The attributes that should be hidden for serialization.
     *
     * @var array<int, string>
     */
    protected $hidden = [
        'password',
        'remember_token',
    ];

    /**
     * The attributes that should be cast.
     *
     * @var array<string, string>
     */
    protected $casts = [
        'email_verified_at' => 'datetime',
    ];

    protected $attributes = [
        'user_role' => 'USER',
        'status' => 'OFFLINE'
    ];

    /**
     * The links that belong to the user.
     *
     * @return \Illuminate\Database\Eloquent\Relations\HasMany<\App\Models\Link>
     */
    public function links()
    {
        return $this->hasMany(Link::class);
    }

    public function connectedToSession()
    {
        return $this->hasOne(DSession::class);
    }

    public static function boot() {
        parent::boot();

        static::deleting(function($link) {
          $link->links()->delete();
        });
    }
}
